// Helper functions
export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug)
}

export function getProductsByCategory(category: string, subcategory?: string): Product[] {
  return products.filter(p => {
    if (subcategory) {
      return p.category === category && p.subcategory === subcategory
    }
    return p.category === category
  })
}

export function getFeaturedProducts(collectionId: string): Product[] {
  const collection = featuredCollections.find(c => c.id === collectionId)
  if (!collection) return []
  return products.filter(p => collection.productIds.includes(p.id))
}

